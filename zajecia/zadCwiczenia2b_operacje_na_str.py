tekst = "Nie mówcie mi więc co mam robić trytytyty"

print(tekst[0:100])

print(tekst[0:10:2]) #od indeksu 0 do 10, co drugi znak - to zero oznacza, że nie bierze tej pierwszej wartości

print(tekst[-1:0:-1]) #cały tekst, od tyłu, bez pierwszego znaku

print(tekst.split(" ")) #['Nie', 'mówcie', 'mi', 'więc', 'co', 'mam', 'robić', 'trytytyty']